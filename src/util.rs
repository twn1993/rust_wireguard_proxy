/// String obfuscation utilities to help avoid static signature-based detection.
/// Uses XOR with a fixed key to hide sensitive strings from static analysis.

const KEY: u8 = 0xAA;

/// Deobfuscate a byte array back into a String using XOR.
pub fn s(data: &[u8]) -> String {
    data.iter().map(|&b| (b ^ KEY) as char).collect()
}

